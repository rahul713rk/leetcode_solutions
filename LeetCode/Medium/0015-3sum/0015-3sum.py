class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        nums.sort()
        res = []

        for i , n in enumerate(nums):
            if n > 0:
                break
            
            if i >0 and nums[i-1] == nums[i]:
                continue
            
            l = i+1
            r = len(nums)-1
            while r > l:
                total = nums[l] + n + nums[r]

                if total > 0:
                    r -= 1
                elif total < 0:
                    l += 1
                else:
                    res.append([nums[l] , n , nums[r]])
                    r -= 1
                    l += 1
                    while l < r and nums[l] == nums[l-1]:
                        l += 1 
            
        return res